package ac.seven.CDN.Syncher.Utils;

public class Coords {

    private int x = 0;
    private int y = 0;
    private int z = 0;

    public Coords(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

    public int getZ() {
        return this.z;
    }
}
